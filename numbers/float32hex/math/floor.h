#ifndef C23META_FP32H_FLOOR
#define C23META_FP32H_FLOOR

#define FP32H_FLOOR(X) _FP32H_FLOOR_C(X)
#define _FP32H_FLOOR_C(X) _FP32H_FLOOR(FP32H_TRUNC(X),X)
#define _FP32H_FLOOR(TX,X) CAT_ALL(_FP32H_FLOOR_X, FP32H_EQ(TX,X), FP32H_SIGN_BIT(X))(TX)
#define _FP32H_FLOOR_X00(TX) TX
#define _FP32H_FLOOR_X01(TX) FP32H_SUB(TX, FP32H_1)
#define _FP32H_FLOOR_X10(TX) TX
#define _FP32H_FLOOR_X11(TX) TX

#endif