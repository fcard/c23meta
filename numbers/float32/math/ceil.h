#ifndef C23META_FP32_CEIL
#define C23META_FP32_CEIL

#define FP32_CEIL(X) _FP32_CEIL_C(X)
#define _FP32_CEIL_C(X) _FP32_CEIL(FP32_TRUNC(X),X)
#define _FP32_CEIL(TX,X) CAT_ALL(_FP32_CEIL_X, FP32_EQ(TX,X), FP32_SIGN_BIT(X))(TX)
#define _FP32_CEIL_X00(TX) FP32_ADD(TX, FP32_1)
#define _FP32_CEIL_X01(TX) TX
#define _FP32_CEIL_X10(TX) TX
#define _FP32_CEIL_X11(TX) TX

#endif