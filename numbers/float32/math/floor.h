#ifndef C23META_FP32_FLOOR
#define C23META_FP32_FLOOR

#define FP32_FLOOR(X) _FP32_FLOOR_C(X)
#define _FP32_FLOOR_C(X) _FP32_FLOOR(FP32_TRUNC(X),X)
#define _FP32_FLOOR(TX,X) CAT_ALL(_FP32_FLOOR_X, FP32_EQ(TX,X), FP32_SIGN_BIT(X))(TX)
#define _FP32_FLOOR_X00(TX) TX
#define _FP32_FLOOR_X01(TX) FP32_SUB(TX, FP32_1)
#define _FP32_FLOOR_X10(TX) TX
#define _FP32_FLOOR_X11(TX) TX

#endif