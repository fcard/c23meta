#ifndef C23META_U32_EXP
#define C23META_U32_EXP
#include "../../util.h"

#define U32_EXP(X,Y) _U32_EXP(X,Y)
#define _U32_EXP(X,Y) _U32_EXP_EVAL(_U32_EXP_PREAMBLE(U32_EQ(Y, U32_0), X, Y))
#define _U32_EXP_EVAL(X) _U32_EXP_EVAL1(_U32_EXP_EVAL1(_U32_EXP_EVAL1(_U32_EXP_EVAL1(X))))
#define _U32_EXP_EVAL1(X) _U32_EXP_EVAL2(_U32_EXP_EVAL2(_U32_EXP_EVAL2(_U32_EXP_EVAL2(X))))
#define _U32_EXP_EVAL2(X) _U32_EXP_EVAL3(_U32_EXP_EVAL3(_U32_EXP_EVAL3(_U32_EXP_EVAL3(X))))
#define _U32_EXP_EVAL3(X) _U32_EXP_EVAL4(_U32_EXP_EVAL4(_U32_EXP_EVAL4(_U32_EXP_EVAL4(X))))
#define _U32_EXP_EVAL4(X) X

#define _U32_EXP_PREAMBLE(G,X,N) DEFER(CAT(_U32_EXP_P,G))(X,N)
#define _U32_EXP_P0(X,N) _U32_EXP_X(X,U32_1,N)
#define _U32_EXP_P1(X,N) U32_1

#define _U32_EXP_X(X,Y,N) _U32_EXP_Y(U32_GT(N,U32_1),X,Y,N)
#define _U32_EXP_Y(G,X,Y,N) DEFER(CAT(_U32_EXP_Z,G))(X,Y,N)
#define _U32_EXP_Z0(X,Y,N) U32_MUL(X,Y)
#define _U32_EXP_Z1(X,Y,N) _U32_EXP_V(U32_IS_ODD(N),X,Y,N)
#define _U32_EXP_V(G,X,Y,N) DEFER(CAT(_U32_EXP_W,G))(X,Y,N)
#define _U32_EXP_W0(X,Y,N) OBSTRUCT(_U32_EXP_I)()(U32_MUL(X,X),Y,U32_RSH1(N))
#define _U32_EXP_W1(X,Y,N) OBSTRUCT(_U32_EXP_I)()(U32_MUL(X,X),U32_MUL(X,Y),U32_RSH1(U32_SUB(N,U32_1)))
#define _U32_EXP_I() _U32_EXP_X

#endif